import path from 'path'

/******************************************************************************/
// Helper
/******************************************************************************/

function lib(dir) {

  dir = path.resolve(dir, '..')
  const name = path.basename(dir)

  return name === 'feathers-schema' ? dir : lib(dir)

}

/******************************************************************************/
// Export
/******************************************************************************/

module.exports = require(lib(__dirname))
